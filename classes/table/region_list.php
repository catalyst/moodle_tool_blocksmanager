namespace tool_blocksmanager\table;

use core\persistent;
use tool_blocksmanager\base_controller;
use tool_blocksmanager\region_controller;

defined('MOODLE_INTERNAL') || die();

require_once($CFG->libdir.'/tablelib.php');

class region_list extends \flexible_table {

    /**
     * @var int Autogenerated id.
     */
    private static $autoid = 0;

    /**
     * Constructor
     *
     * @param string|null $id to be used by the table, autogenerated if null.
     */
    public function __construct($id = null) {
        global $PAGE;

        $id = (is_null($id) ? self::$autoid++ : $id);
        parent::__construct('tool_blocksmanager_'.$id);

        $this->define_baseurl($PAGE->url);
        $this->set_attribute('class', 'generaltable admintable');

        $this->define_columns($this->get_columns());

        $headers = [];
        foreach ($this->get_columns() as $column) {
            $headers[] = get_string('col_' . $column, 'tool_blocksmanager');
        }

        $this->define_headers($headers);

        $this->setup();
    }

    /**
     * Return a list of all columns;
     * @return array
     */
    protected function get_columns() {
        return [
            'region',
            'categories',
            'config',
            'remove',
            'hide',
            'add',
            'move',
            'actions'
        ];
    }

    /**
     * Display column.
     *
     * @param persistent $record
     * @return string
     */
    public function col_region(persistent $record) {
        return $this->get_display_value($record, 'region');
    }

    /**
     * Display column.
     *
     * @param persistent $record
     * @return string
     */
    public function col_categories(persistent $record) {
        return $this->get_display_value($record, 'categories');
    }

    /**
     * Display column.
     *
     * @param persistent $record
     * @return string
     */
    public function col_config(persistent $record) {
        return $this->get_display_value($record, 'config');
    }

    /**
     * Display column.
     *
     * @param persistent $record
     * @return string
     */
    public function col_remove(persistent $record) {
        return $this->get_display_value($record, 'remove');
    }

    /**
     * Display column.
     *
     * @param persistent $record
     * @return string
     */
    public function col_hide(persistent $record) {
        return $this->get_display_value($record, 'hide');
    }

    /**
     * Display column.
     *
     * @param persistent $record
     * @return string
     */
    public function col_add(persistent $record) {
        return $this->get_display_value($record, 'add');
    }

    /**
     * Display column.
     *
     * @param persistent $record
     * @return string
     */
    public function col_move(persistent $record) {
        return $this->get_display_value($record, 'move');
    }

    /**
     * Get value to display.
     *
     * @param persistent $record Region record
     * @param string $col Col name.
     *
     * @return mixed|string
     * @throws \coding_exception
     */
    protected function get_display_value(persistent $record, $col) {
        $value = $record->get($col);

        switch ($col) {
            case 'categories':
                $catids = explode(',', $value);
                $categories = [];
                foreach (\core_course_category::get_many($catids) as $category) {
                    if (!empty($category->name)) {
                        $categories[] = $category->name;
                    }
                }

                $display = implode('<BR />', $categories);

                break;

            case 'config':
            case 'remove':
            case 'hide':
            case 'add':
            case 'move':
                $display = empty($value) ? get_string('no') : get_string('yes');
                break;

            default:
                $display = $value;
        }

        return $display;
    }

    /**
     * Display column.
     *
     * @param persistent $record
     * @return string
     */
    public function col_actions(persistent $record) {
        $buttons = [];

        $buttons[] = self::format_icon_link(
            new \moodle_url($this->get_base_url(), [
                'action' => base_controller::ACTION_EDIT,
                'id' => $record->get('id'),
            ]),
            't/edit',
            get_string('edit')
        );

        $buttons[] = self::format_icon_link(
            new \moodle_url($this->get_base_url(), [
                'id' => $record->get('id'),
                'sesskey' => sesskey(),
                'action' => base_controller::ACTION_DELETE,
            ]),
            't/delete' ,
            get_string('delete')
        );

        return \html_writer::tag('nobr', implode('&nbsp;', $buttons));
    }

    /**
     * Return base URL for action buttons.
     * @return string
     */
    protected function get_base_url() {
        return region_controller::get_base_url();
    }

    /**
     * Format icon link.
     *
     * @param string $url The URL for the icon.
     * @param string $icon The icon identifier.
     * @param string $alt The alt text for the icon.
     * @param string $iconcomponent The icon component.
     * @param array $options Display options.
     *
     * @return  string
     */
    public static function format_icon_link($url, $icon, $alt, $iconcomponent = 'moodle', $options = array()) {
        global $OUTPUT;

        return $OUTPUT->action_icon(
            $url,
            new \pix_icon($icon, $alt, $iconcomponent, ['title' => $alt]),
            null,
            $options
        );
    }

    /**
     * Sets the data of the table.
     *
     * @param persistent[] $records  An array with records.
     */
    public function display(array $records) {
        foreach ($records as $record) {
            $this->add_data_keyed($this->format_row($record));
        }

        $this->finish_output();
    }

    /**
     * @inheritdoc
     */
    public function print_nothing_to_display() {
        echo \html_writer::div(get_string('no_regions', 'tool_blocksmanager'));
    }

}